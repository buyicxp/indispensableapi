package com.buyi.indispensable.entity;


public class Withdraw {

  private long wId;
  private long uId;
  private double wMonew;
  private long wState;


  public long getWId() {
    return wId;
  }

  public void setWId(long wId) {
    this.wId = wId;
  }


  public long getUId() {
    return uId;
  }

  public void setUId(long uId) {
    this.uId = uId;
  }


  public double getWMonew() {
    return wMonew;
  }

  public void setWMonew(double wMonew) {
    this.wMonew = wMonew;
  }


  public long getWState() {
    return wState;
  }

  public void setWState(long wState) {
    this.wState = wState;
  }

}
